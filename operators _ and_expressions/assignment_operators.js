console.log("assignment_operator");

let assignment = 1;
assignment  += 5 // same as assignment = assignment + 5
console.log("value is now = ", assignment);

assignment -=5; //same as assignment = assignment - 5
console.log("value is now = ", assignment);


assignment /=5 //same as assignment = assignment / 5
console.log("value is now = ", assignment);

assignment *=5 // same as assignment = assignment * 5
console.log("value is now = ", assignment);

